// M=0..11  O=12..20  N=21..31  K=32..40  E=41..49  Y=50+
const COLS = [
  { color: 'orange', start: 0,  end: 11  },
  { color: 'pink',   start: 12, end: 20  },
  { color: 'yellow', start: 21, end: 31  },
  { color: 'green',  start: 32, end: 40  },
  { color: 'blue',   start: 41, end: 49  },
  { color: 'teal',   start: 50, end: 999 },
]

function colorLine(line) {
  let out = ''
  for (let i = 0; i < line.length; i++) {
    const col = COLS.find(c => i >= c.start && i <= c.end)
    out += C.bold + C[col ? col.color : 'cream'] + line[i] + C.reset
  }
  return out
}

